var copicXCatalog = new Object();
var copicXPrefix = "_copiccatx_";
var copicXCatLen = copicXPrefix.length;

// get xcat for name
function cpx_getXCat(name) {
	if (copicXCatalog[name] === undefined) {
		copicXCatalog[name] = new Object();
		copicXCatalog[name].exclusions = new Array();
		copicXCatalog[name].hasMarker = false;
		copicXCatalog[name].hasRefill = false;
	}
	return copicXCatalog[name];
}

// save one copic xcat to local storage
function cpx_saveXCat(name) {
	localStorage.setItem(copicXPrefix + name, JSON
			.stringify(copicXCatalog[name]));
}

// for given code, indicate whether you have the marker and refill
function cpx_setMarkerInventory(code, hasMarker, saveNow) {
	var copicObj = cpx_getXCat(code);
	copicObj.hasMarker = hasMarker;
	if (saveNow) {
		cpx_saveXCat(code);
	}
}

// for given code, indicate whether you have the marker and refill
function cpx_setRefillInventory(code, hasRefill, saveNow) {
	var copicObj = cpx_getXCat(code);
	copicObj.hasRefill = hasRefill;
	if (saveNow) {
		cpx_saveXCat(code);
	}
}

// if isEx, add dest to source exclusion list and vice versa
// else, remove dest from source exclusion list and vice versa
function cpx_setExclusion(source, dest, isEx, saveNow) {
	var sourceObj = cpx_getXCat(source);
	var destObj = cpx_getXCat(dest);
	var idxSourceDest = sourceObj.exclusions.indexOf(dest);
	var idxDestSource = destObj.exclusions.indexOf(source);

	if (idxSourceDest < 0) {
		if (idxDestSource >= 0) {
			throw "Exclusion error: dest has source, source does not have dest *"
					+ source + "*" + dest + "*";
		}
		if (isEx == false) {
			throw "Exclusion error: attempt to include but no exclusion exists *"
					+ source + "*" + dest + "*";
		}
		sourceObj.exclusions.push(dest);
		destObj.exclusions.push(source);
	} else {
		if (idxDestSource < 0) {
			throw "Exclusion error: source has dest, dest does not have source *"
					+ source + "*" + dest + "*";
		}
		if (isEx == true) {
			throw "Exclusion error: attempt to exclude but exclusion exists *"
					+ source + "*" + dest + "*";
		}

		// remove from exclusion arrays
		sourceObj.exclusions.splice(idxSourceDest, 1);
		destObj.exclusions.splice(idxDestSource, 1);
	}
	if (saveNow == true) {
		cpx_saveXCat(source);
		cpx_saveXCat(dest);
	}
}

// helper function - return true if source has dest as exclusion
function cpx_hasExclusion(source, dest) {
	var sourceObj = cpx_getXCat(source);
	var idxSourceDest = sourceObj.exclusions.indexOf(dest);
	return idxSourceDest >= 0;
}

