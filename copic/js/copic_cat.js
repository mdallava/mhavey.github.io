// load recipe into memory
function cpx_loadXCat(name, obj) {
	if (name.indexOf(copicXPrefix) == 0) {
		copicXCatalog[name.substring(copicXCatLen)] = obj;
	}
	return false;
}

// clear xcat in mem
function cpx_clearAllXCat() {
	copicXCatalog = new Object();
}

// delete specified xcat
function cpx_deleteXCat(item) {
	if (name.indexOf(copicXPrefix) == 0) {
		localStorage.removeItem(name);
		return true;
	}
	return false;
}

// export xcat to an object to export
function cpx_exportXCat(item) {
	if (item.indexOf(copicXPrefix) == 0) {
		return {
			type : "xcat",
			name : item.substring(copicXCatLen),
			obj : JSON.parse(localStorage[item])
		};
	} else {
		return null;
	}
}

// import xcat from specified obj
function cpx_importXCat(obj) {
	if (obj.type == "xcat") {
		var name = obj.name;
		var val = obj.obj;
		localStorage.setItem(copicXPrefix + name, JSON.stringify(val));
		copicXCatalog[name] = val;
		return true;
	}
	return false;
}
